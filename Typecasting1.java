//Narrow typecasting

import java.io.*;
class Typecasting1{
public static void main(String args[]){
double i=100.245;
short j=(short)i;
int k=(int)i;
System.out.println("Original Value before casting"+i);
System.out.println("After Type casting to short" +j);
System.out.println("After Type casting to int"+k);
}
}